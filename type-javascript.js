// Number
let x = 10;
console.log(typeof x); // "number"

// Change to String
x = "hello";
console.log(typeof x); // "string"

// Change to Boolean
x = true;
console.log(typeof x); // "boolean"

// Null
let y = null;
console.log(typeof y); // "object" (this is a historical bug in JavaScript)

// Undefined
let z;
console.log(typeof z); // "undefined"

